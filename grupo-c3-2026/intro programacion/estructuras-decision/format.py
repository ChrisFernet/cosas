# FORMAT
print("Julian tiene {0} años".format(25))
print("El valor de pi es: {0}". format(3.1415926))
print("El valor de pi es: {0:.2f}". format(3.1415926))
print("Oscar gano el baloto gringo por valor de: USD${0:,.2f}". format(5000000))
print("El {0:.2%} de campers se gradua en campus". format(0.9897))

# CADENAS f-string
 
print(f"Julian tiene {25} años")
print(f"El valor de pi es: {3.1415926}")
print(f"El valor de pi es: {3.1415926:.2f}")
print(f"Oscar gano el baloto gringo por valor de: USD${5000000:,.2f}")
